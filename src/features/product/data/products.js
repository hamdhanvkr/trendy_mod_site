export const getFeaturedProducts = () => {
    return products.filter(product => product.isFeatured === true);
};

export const getProductsByCategory = (categoryId) => {
    return products.filter(product => product.category === categoryId);
};

export const getPopularProducts = () => {
    return products.filter(product => product.isPopular === true);
};

export const getNewProducts = () => {
    return products.filter(product => product.isNew === true);
};

export const getOriginalPrice = (finalPrice, discount) => {
    return Math.round(finalPrice / (1 - discount / 100));
};

export const getDiscountedPrice = (price) => {
    return price;
};

export const getDiscountAmount = (price, discount) => {
    const originalPrice = getOriginalPrice(price, discount);
    return originalPrice - price;
};

export const getOriginalPriceForDisplay = (price, discount) => {
    if (discount === 0) return null;
    return getOriginalPrice(price, discount);
};

